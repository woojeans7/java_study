package ch01.sec11;

public class Calculator {
    public static void main(String[] args) {
        // 변수 x에 1 지정, y에 2 배정
        int x = 1;
        int y = 2;
        // 변수 result에 변수 x, y의 합을 저장
        int result = x + y;
        // 변수 result를 화면에 출력하세요
        System.out.println(result);
    }
}
